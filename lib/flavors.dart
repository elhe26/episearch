enum Flavor {
  PRODUCTION,
  DEVELOPMENT,
}

class F {
  static Flavor appFlavor;

  static String get title {
    switch (appFlavor) {
      case Flavor.PRODUCTION:
        return 'EpiSearch';
      case Flavor.DEVELOPMENT:
        return 'EpiSearch Dev';
      default:
        return 'title';
    }
  }

}
